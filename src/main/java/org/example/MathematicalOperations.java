package org.example;

public class MathematicalOperations {
    public int addition (int numberA, int numberB) {
        return numberA + numberB;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int compare(int n1, int n2) {
        if (n1 > n2) return 1;
        return -1;
    }

}
